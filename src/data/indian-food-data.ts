// Get greeting based on time
export function getGreeting(language: 'english' | 'hindi' | 'both' = 'both'): { en: string; hi: string; emoji: string } {
  const hour = new Date().getHours();
  
  if (hour >= 5 && hour < 12) {
    return { en: 'Good morning', hi: 'सुप्रभात', emoji: '🌅' };
  } else if (hour >= 12 && hour < 17) {
    return { en: 'Good afternoon', hi: 'नमस्ते', emoji: '🍛' };
  } else if (hour >= 17 && hour < 21) {
    return { en: 'Good evening', hi: 'शुभ संध्या', emoji: '🌆' };
  } else {
    return { en: 'Good night', hi: 'शुभ रात्रि', emoji: '🌙' };
  }
}

// Get meal context based on time
export function getMealContext(): { meal: MealType; en: string; hi: string } {
  const hour = new Date().getHours();
  
  if (hour >= 6 && hour < 11) {
    return { meal: 'breakfast', en: 'Breakfast time!', hi: 'नाश्ते का समय!' };
  } else if (hour >= 11 && hour < 15) {
    return { meal: 'lunch', en: 'Lunch time!', hi: 'दोपहर के खाने का समय!' };
  } else if (hour >= 15 && hour < 19) {
    return { meal: 'snacks', en: 'Snack time!', hi: 'नमकीन का समय!' };
  } else {
    return { meal: 'dinner', en: 'Dinner time!', hi: 'रात के खाने का समय!' };
  }
}

// Get price display
export function formatPrice(price: { min: number; max: number }): string {
  if (price.min === price.max) {
    return `₹${price.min}`;
  }
  return `₹${price.min} - ₹${price.max}`;
}

// Get spice display
export function getSpiceDisplay(level: string): string {
  const levels: Record<string, string> = {
    'mild': '🌶️',
    'medium': '🌶️🌶️',
    'spicy': '🌶️🌶️🌶️',
    'extra-spicy': '🌶️🌶️🌶️🌶️',
  };
  return levels[level] || '🌶️';
}
