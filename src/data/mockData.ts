const generateExpenses = (): Expense[] => {
  const expenses: Expense[] = [];
  const restaurants = [
    { name: 'Punjabi Tadka', cuisine: 'Indian', priceRange: [350, 650] },
    { name: 'Starbucks', cuisine: 'Cafe', priceRange: [180, 380] },
    { name: 'China Gate', cuisine: 'Chinese', priceRange: [400, 800] },
    { name: 'Saravana Bhavan', cuisine: 'South Indian', priceRange: [150, 350] },
    { name: 'Olive Garden', cuisine: 'Italian', priceRange: [800, 2000] },
    { name: "McDonald's", cuisine: 'Fast Food', priceRange: [150, 350] },
    { name: "Karim's", cuisine: 'Mughlai', priceRange: [450, 850] },
    { name: 'Barbeque Nation', cuisine: 'BBQ', priceRange: [1200, 2500] },
    { name: 'Thai Pavilion', cuisine: 'Thai', priceRange: [500, 900] },
    { name: 'Domino\'s Pizza', cuisine: 'Italian', priceRange: [300, 600] },
    { name: 'Paradise Biryani', cuisine: 'Hyderabadi', priceRange: [250, 450] },
    { name: 'Chaayos', cuisine: 'Cafe', priceRange: [100, 250] },
  ];

  const categories = ['Dine-in', 'Delivery', 'Takeout', 'Coffee & Drinks', 'Fast Food', 'Desserts'];
  const paymentMethods = ['UPI', 'Card', 'Cash', 'Wallet'];
  const notes = [
    'Lunch with friends', 'Quick breakfast', 'Dinner date', 'Team lunch', 
    'Office treat', 'Weekend brunch', 'Late night snack', 'Birthday party',
    'Work from cafe', 'Movie night', 'Family dinner', 'Solo treat', ''
  ];

  for (let i = 0; i < 30; i++) {
    const daysAgo = Math.floor(Math.random() * 30);
    const hoursAgo = Math.floor(Math.random() * 24);
    const restaurant = restaurants[Math.floor(Math.random() * restaurants.length)];
    const amount = Math.floor(Math.random() * (restaurant.priceRange[1] - restaurant.priceRange[0])) + restaurant.priceRange[0];
    const category = categories[Math.floor(Math.random() * categories.length)];
    const paymentMethod = paymentMethods[Math.floor(Math.random() * paymentMethods.length)];
    const note = notes[Math.floor(Math.random() * notes.length)];
    
    const date = new Date();
    date.setDate(date.getDate() - daysAgo);
    date.setHours(hoursAgo);

    const hasSplit = Math.random() > 0.85;
    const splitPeople = hasSplit ? Math.floor(Math.random() * 4) + 2 : undefined;

    expenses.push({
      id: `exp-${i + 1}`,
      amount,
      category,
      cuisine: restaurant.cuisine,
      restaurant: restaurant.name,
      date,
      paymentMethod,
      notes: note,
      ...(hasSplit && splitPeople ? {
        splitBill: {
          total: amount * splitPeople,
          people: splitPeople,
          yourShare: amount,
        }
      } : {}),
    });
  }

  return expenses.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
};

